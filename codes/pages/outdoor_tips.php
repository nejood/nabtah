 <!-------- TIP # 1 ------->
				<p class="tips">
<b class="tip_point"> 1. Make sure your plants are getting enough water.</b> <br>
 Caring for plants in a garden relies heavily on the natural elements and environment in proximity. As a result, the amount of watering you need to do is dependent on what the weather and soil is like in your area. It’s generally a good rule of thumb to water your plants 2-3 times a week either by hand with a watering can, or with a sprinkler system. The soil of your garden should be damp without being soggy, and not so dry that it is crumbly and dusty.
 <ul class="tips tipsList">
<li> Check the ideal watering amounts for each plant, as some varieties prefer lots of water while others require very little. </li>
</ul>	
</p>

 <!-------- TIP # 2 ------->
<p class="tips">
<b class="tip_point">2. Weed your garden regularly.</b> <br>
 Weeds can spring up overnight and ruin a perfectly lovely garden. Weeds aren’t only an eyesore, they also take up valuable growing space and use nutrients in the soil that could otherwise go towards growing your garden. As a result, you should try to pull weeds whenever you see them pop up. Grasp each weed as close to the ground as you’re able, and then pull it straight up. doing this will increase the likelihood of pulling out the root system and slowing the growth of future weeds.
 <ul class="tips tipsList">
<li>You can use weed killers in your garden, but most aren’t plant-specific and will kill all surrounding plants (not just the weeds).</li>
<li>Check for weeds growing underneath the canopy of a plant or bush.</li>
</ul>	
</p>

 <!-------- TIP # 3 ------->
<p class="tips">
<b class="tip_point"> 3. Mulch your garden every few months.</b> <br>
 Mulch is a type of organic compost that is added to the top of your garden soil to prevent weeds from growing and to trap moisture. Mulching will also add nutrients to the soil as it mixes in over time, helping your plants to grow even larger. You can purchase mulch at most garden supply stores. Simply add a layer of mulch 1–2 inches (2.5–5.1 cm) thick over the entirety of the topsoil in your garden.
 <ul class="tips tipsList">
<li>Be careful not to cover up the base of your plants with mulch, as this will inhibit their growth. This is especially important for small trees and bushes.</li>
<li>You can add a layer of organic compost as a substitute for garden mulch, if you want. Keep in mind that compost will not prevent weeds like mulch will, although it is beneficial to your plant's growth.</li>
<li>For permanent plantings, you can lay down landscape fabric over the soil to act as a weed barrier. Using this in combination with mulch will reduce weeds to almost nothing.</li>
</ul>
</p>

 <!-------- TIP # 4 ------->
<p class="tips">
<b class="tip_point">4. Cut off any dead or diseased plants.</b> <br>
 Plant disease can spread quickly through a garden, if not contained. The same is true for a plant that is injured; if you don’t remove the dying limbs, it can continue to spread to the rest of the plant. Whenever you notice plants that are browning, dry, brittle, or otherwise sickly looking, use a pair of gardening shears to cut off the branches from the base. Throw these branches away rather than leaving them in your garden as compost, because if they do contain a plant disease, it can still spread to nearby plants.
</p>

 <!-------- TIP # 5 ------->
<p class="tips">
<b class="tip_point">5. Deadhead your flowers.</b> <br>
The term ‘deadhead’ refers to the practice of cutting off the dead flower heads on a plant. This will stimulate new growth, and remove the brown and dying flower. To do this, simply use a pair of gardening shears to snip the flower just below the bud. Over the next few days, you should notice a new bud begin to form and bloom.
<ul class="tips tipsList">
<li> To encourage the growth of leaves, cut flower buds before they bloom. This will conserve the plant's energy.
</li>
</ul>
</p>

 <!-------- TIP # 6 ------->
<p class="tips">
<b class="tip_point">6. Fertilize your plants once a month.</b> <br>
 Outdoor plants get more nutrients from their surrounding environment than do indoor plants, meaning that they need to be fertilized much less. Find a fertilizer that meets your plants specific mineral requirements, or choose an ‘average’ fertilizer such as a 6-12-6 or 10-10-10 mixture from your local nursery. Spray or sprinkle the fertilizer onto the plants once every 4-5 weeks, according to package directions.
 <ul class="tips tipsList">
<li>Mix the fertilizer into the top few inches of soil. This can help reduce harmful runoff of fertilizers into waterways and other areas. If fertilizer is left on top of the soil, the nutrients will enter the air instead of the soil.</li>
<li>Consider organic options for fertilizing. Most nutrition contained in chemical fertilizers is wasted because it is released faster than plants can use it. Furthermore, they only provide three nutrients. Mature organic compost or organic mixes provide more micronutrients. Organic compost will release these nutrients over a long period as plants need them.</li>
<li>If you’re unsure as to what fertilizer to use, ask your local nursery worker for help.</li>
</ul>
</p>
