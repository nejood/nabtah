    		<b class="tip_point">About the Areca Palm</b> <br>
    The Areca Palm or Butterfly Palm is a relatively easy care indoor houseplant that has much going for it. Its attractive nature has been awarded the Royal Horticultural Society's Award of Garden Merit, which for a plant grown more commonly indoors than out, is a pretty rare feat.

The Areca Palm is one of the most effective air cleaning houseplantsIt goes by many names - Areca lutescens, Chrysalidocarpus lutescens, Dypsis lutescens, Butterfly Palm (because the fronds resemble butterflies), Yellow Palm, Golden Cane (because the lower canes have a gold / yellow colour) and of course Areca Palm.

The three most well known and popular indoor palms are the Sentry / Kentia Palms, the Parlour / Parlor Palm and the Areca Palm, of these perhaps the Areca Palm is the most difficult, however even then it's not that hard.

Appearance wise It can easily be separated from other types of palms because of its narrow, numerous full fronds arranged close to one another in neat compact lines along the stems. The other types mentioned above have wider and more sparse fronds which means all three have a distinct appearance when compared side by side.

The Areca Palm's popularity as an office and houseplant increased (and is maintained today) in part because of the air cleaning experiments carried out by NASA and Dr Wolverton in the 1980's.

50 plants were tested for their ability to filter the air around us, ease of care and ability to add moisture to the surrounding atmosphere. The Areca Palm took first place scoring highly in all categories meaning it was ranked the best houseplant at cleaning the air in our homes and offices.

Good quality Areca Palms for sale at a nursery taken by Mokkie
Bold, attractive, easy to care for, desirable and positive for our health. So what's not to like? The fashion for these plants is that "bigger is better" and this has persisted over the last few decades. Which means because few people want to buy small Areca's, few people actually sell them.

Therefore you can only really buy these palms in a large size and this results in firstly needing the actual space to home one and an initial financial outlay. In fact, they may be one of the most expensive houseplants you can buy second only to Orchids.

If you're brand new to houseplants you might want to wait until your green thumb has greened up a little, as it would be a shame to lose one through poor treatment or care resulting from inexperience. However if you're confident, have the space and want something striking, the Areca Palm or Butterfly Palm is a plant you will definitely want on your shortlist.
