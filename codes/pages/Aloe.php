    		<b class="tip_point">About the Aloe Vera Plant</b> <br>
    The Aloe plant is one of the more commonly known houseplants because of the Aloe Vera gel you find inside its leaves. Aloe Vera houseplant in a bright sunny windowIf you go into any chemist or department store, chances are you will find a product for sale which contains Aloe Vera gel and has a picture of the plant on the packaging.

Aloe Vera Gel is sold to treat a lot of different aliments, from burns to helping support the health of the digestive system and because of the gels popularity it's not surprising that the plant itself has become easily recognisable.

We don't recommend only buying an Aloe Vera plant for its purported medicinal uses, because this indoor plant is actually fantastically easy to care for and a mature well cared for Aloe Vera can look incredibly striking and beautiful. Obviously the gel aspect may have have some advantages, so by all means keep the plant at hand in the event you get a small burn.

When it comes to other possible medical uses, we do get asked a lot if "the Aloe Vera plant is edible?" This is because it's reported to help with stomach complaints. Is this Aloe Vera plant actually edibleWe really don't want to encourage you to cut bits off to eat, unless you truly know what you are doing (some parts of the Aloe Vera plant are toxic and others can create laxative effects!)

If you're a serious Aloe Vera Gel believer it's best if you stick to the official products that have been created specifically for purpose. So again please no D.I.Y. experiments with the leaves unless you know what you're doing.

There are several different types of Aloe Vera plants you can buy and even more types of Aloe available. The majority make good houseplants, and grow very fast when compared to other succulents. In general unless you are searching in specialist shops the plant will simply be labeled as "A. vera". Different types can be sought although they all have similar care requirements as detailed below.</p>

