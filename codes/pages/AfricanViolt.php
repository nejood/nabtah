    		<b class="tip_point">About the African Violet</b> <br>
    Saintpaulia is often know as the African Violet because it originates from Africa and the flowers look like violets (although they are African Violet plant with purple flowers unrelated). The plant is a newish addition to our homes, only appearing in large numbers starting in America in the 1930's but has been loved ever since.

The plant was discovered by Baron von Saint Paul in 1892 at which point it was sent to Hermann Wendland at Berlin's Royal Botanic Gardens, who named it Saintpaulia ionantha after Baron's family name.

The original Saintpaulia was quite difficult to grow in homes and consequently didn't become popular. However around 1925 Armacost and Royston, a Los Angeles nursery, recognised the potential of the plant and started developing new cultivars.

It wasn't long before they released their first creation named "blue boy", it was quite different to the older Saintpaulia because this variety was easy to propagate, could deal quite happily with average home conditions and had a longer and more robust flowering period. The Americans loved it and from 1930 onwards it became increasingly desired.
Gradually more cultivars were created and cross-bred, as the years passed new colours and plants with different leaf shapes came to the market. African Violet's didn't become very popular in Europe until after the end of the Second World War at which point countries like United Kingdom became gripped throughout the 1960s and 1970s by, what was dubbed, African Violet fever.
This was in part due to Dr David Hessayon and his best selling gardening books, that opened a door to show people that looking after houseplants like the African Violet was actually incredibly easy.

Many plants have their devoted fans, you just don't know about it because the majority aren't that vocal about it. The Saintpaulia fans though are a different kettle of fish, African Violet plant with pink flowersthey adore this plant and have a passionate desire to come together and share their experiences as well as help new amateur owners find their way. African Violet Societies have existed in many countries around the world since the end of the Second World War.

The plant is clearly steeped in a rich history and although you may assume therefore that it's automatically old fashioned, the popularity is still prevalent today. African Violets can be readily found in shops at very cheap prices.

Being compact and low growing they can suit multiple locations. Propagation is easy and fast, with the blooms having no specific flowering season, which means it can be in flower any time of the year and for long periods.</p>
