 <!-------- TIP # 1 ------->
<p class="tips">
<b class="tip_point"> 1. Add drainage to poorly draining soil.</b> <br>
 If your garden patch or potted plant constantly has a pool of water sitting on top, then the soil is poorly draining. This is bad, because the buildup of water can cause the roots of the plant to rot, killing the plant over time. To remedy this, carefully dig out the plant and a clump of the surrounding soil; place the plant on a tarp or in another clean pot. Mix compost or peat into the soil. Replace your plant in its original location.
 <ul class="tips tipsList">
<li>If all of your soil is poorly draining, you can dig it up and mix in sand to help increase the drainage.</li>
</ul>
</p>

<!-------- TIP # 2 ------->
<p class="tips">
<b class="tip_point"> 2. Move plants that are placed too closely together.</b> <br>
 If you got a bit over-enthusiastic and planted several plants near each other when they were small, you may have been surprised when they grew up and began competing for space in your garden or indoor pot. Plants that are put too close together will not grow as large, since there are not enough nutrients to be shared between them. Simply dig up the offending plants, and relocate them to a new garden plot or pot that has more room. Fill in the empty space with fresh gardening soil.
 <ul class="tips tipsList">
<li> Always use store-bought gardening soil rather than soil from your yard, as the soil from your yard contains insects, plant diseases, and weeds that will spread to your plant in its new location.</li>
<li>You can tell that plants are too close if they are growing into each other, or if their primary stems/branches become tangled.</li>
</ul>
</p>

<!-------- TIP # 3 ------->
<p class="tips">
<b class="tip_point">3. Avoid adding too much mulch. </b><br>
While mulching is beneficial for adding nutrients and blocking out weeds, adding too much mulch can be problematic for a garden. This is because the mulch will not only block out weeds, but will also prevent new growth from your plants from breaking the surface. Never add more than a 2-inch thick layer of mulch to your garden. If your garden isn’t growing after you’ve mulched, take off 1–2 inches (2.5–5.1 cm) of mulch and wait a few weeks for improvement.
<ul class="tips tipsList">
<li>If you add mulch too high on the base of a stem or tree, it will cut off valuable sunlight and prevent growth. Move mulch off the base of the stems and tree trunks in your garden.</li>
</ul>
</p>

<!-------- TIP # 4 ------->
<p class="tips">
<b class="tip_point">4. Cut off any dead or diseased plants. </b><br>
 Plant disease can spread quickly through a garden, if not contained. The same is true for a plant that is injured; if you don’t remove the dying limbs, it can continue to spread to the rest of the plant. Whenever you notice plants that are yellow, browning, dry, brittle, or otherwise sickly looking, use a pair of gardening shears to cut off the branches from the base.
 <ul class="tips tipsList">
<li> Throw these branches away rather than leaving them in your garden as compost, because if they do contain a plant disease, it can still spread to nearby plants.</li>
</ul>
</p>

<!-------- TIP # 5 ------->
<p class="tips">
<b class="tip_point"> 5. Avoid over-watering your plants.</b> <br> 
 Although you may think you’re watering your plants correctly, if they begin to turn yellow and droop, you’re probably giving them too much water. Most plant’s don’t need to be watered daily, in fact they do better when given a lot of water every few days. Only water your plants when the soil is dry at least 2-inches deep. If you water every time the top of the soil looks dry, you are almost guaranteed to over-water your plant. If you are having difficulty giving too much water to indoor plants, try switching to a spray bottle for watering your plants, rather than using a watering can. Spray bottles make it much harder to add too much water, since very little is released at one time.
</p>

<!-------- TIP # 6 ------->
<p class="tips">
<b class="tip_point"> 6. Make sure you don’t plant your plants too deeply.</b> <br>
If your plants are slowly dying and wilting for no known reason, you may have buried them too deeply. The roots of the plant need to be relatively close to the surface, so that they can extract all the nutrients from the topsoil and have access to the sun. Carefully dig up your plants, and replant them so that the root ball is just-at or just-below the surface of the soil. If the root ball is partially exposed, spread a thin layer of mulch over the top to protect it.
<ul class="tips tipsList">
<li>When transplanting a plant from a pot to the garden, keep the soil level at the same depth that it was in the original pot.</li>
<li> If your roots are mostly above the surface, your plant will die as well. You want the roots to be level with the garden topsoil.</li>
</ul>
</p>
