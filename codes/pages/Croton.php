    		<b class="tip_point">About the Croton Plant</b> <br>
    The Croton is a difficult diva of a houseplant to have in your home. There we've said it. Grown outside in its native country of Sri Lanka it's much easier, but indoors it's incredibly temperamental and will not accept poor treatment for very long.

Croton leaves come in many shapes, sizes and coloursBut if you work with it, the Croton is a work of art and can be an extraordinarily showpiece. Our article will give you top tips and care instructions to keep yours looking splendid.

We don't recommend the Croton, or any Codiaeum plants, for the beginner because grown as a houseplant they're a challenge. It's sometimes hard to look after and can be a real bind to even try and find someone who is selling it, but a challenge is sometimes a good thing. (Like growing a Bird of Paradise from seed and having to wait 5 years for it to flower for the first time!).

Finally the last obvious negative point is its family connection with Euphorbia. Possibly the most famous plant family for being mildly poisonous and an irritant to humans and animals. That said, keep the plant out of reach of pets and children and avoid contact with the latex sap it produces when the leaves are cut or damaged and disaster will be avoided.

The leaves are stunning, unique and brilliant. There are several different Croton varieties and cultivars you can find with a good four or five different leaf shapes available. They vary in colour and shape, wide, narrow, lobed or whole are all attainable.

The leaves tend to feel like leather or waxy with a shine that emphases the colours that they come in, ranging from yellow, green orange to dark purple. The leaf patterns are often also mixed in some way, spotted, veined, striped or speckled which is the reason for its common name of Joseph's Coat. This Codiaeum Croton has stunning yellow and orange leaves

What's most interesting of all perhaps is that everything on the plant (except the actual leaf shape), can be a random mix up. This means it's possible to find Crotons which have one colour at the top and totally different at the bottom. See the photo above to get an idea of this.

This huge range makes it really, really difficult to know exactly what varieties or cultivar you actually have. Even if the pots are labeled they may be incorrect, so when you choose your plant it's probably best to look for a healthy one and use your eyes.

Pick the plant you like visually rather than searching for specially named varieties, once your eyes have chosen and you cast them down to the labels, names such as Codiaeum reidii, C 'Eugene Drapps', C. holufiana, C. 'Geduldig', C. 'Goldfinger' and C. 'Norma' are all common.
